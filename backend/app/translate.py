# 预排序（长度降序）用于贪心匹配
EQUIPMENT_SORTED = sorted(EQUIPMENT.keys(), key=len, reverse=True)
MOVEMENT_SORTED = sorted(MOVEMENT.keys(), key=len, reverse=True)


def translate_name(name: str) -> str:
    """把英文动作名翻成中文。未命中返回原英文（保证不丢字段）。"""
    if not name:
        return name
    key = name.strip().lower()
    if key in EXACT_OVERRIDE:
        return EXACT_OVERRIDE[key]

    # 剥离标点，避免 (kneeling) / 's 之类粘连
    s = re.sub(r"[^\w\s]", " ", key)
    s = s.replace("-", " ").replace("/", " ").replace("  ", " ")

    # 1) 装备前缀（可能多个，循环匹配）
    equip_parts = []
    while True:
        hit = False
        for ph in EQUIPMENT_SORTED:
            if ph in s:
                equip_parts.append(EQUIPMENT[ph])
                s = s.replace(ph, " ", 1)
                hit = True
                break
        if not hit:
            break
    equip = "".join(equip_parts)

    # 2) 动作主体（一个名字可能含多个动作，循环匹配）
    movement_parts = []
    while True:
        hit = False
        for ph in MOVEMENT_SORTED:
            if ph in s:
                movement_parts.append(MOVEMENT[ph])
                s = s.replace(ph, " ", 1)
                hit = True
                break
        if not hit:
            break
    movement = "".join(movement_parts)

    # 3) 修饰词 + 其余词
    prefix_mods, suffix_mods, pre_words, post_words = [], [], [], []
    for tok in s.split():
        if not tok:
            continue
        if tok in MODIFIER_PREFIX:
            prefix_mods.append(MODIFIER_PREFIX[tok])
        elif tok in MODIFIER_SUFFIX:
            suffix_mods.append(MODIFIER_SUFFIX[tok])
        elif tok in MUSCLE_WORD:
            pre_words.append(MUSCLE_WORD[tok])
        elif tok in WORD_MAP:
            mapped = WORD_MAP[tok]
            if mapped:
                post_words.append(mapped)
        else:
            cand = tok
            if cand.endswith("s") and len(cand) > 3:
                c2 = cand[:-1]
                if c2 in MODIFIER_PREFIX:
                    prefix_mods.append(MODIFIER_PREFIX[c2]); continue
                if c2 in MUSCLE_WORD:
                    pre_words.append(MUSCLE_WORD[c2]); continue
                if c2 in WORD_MAP and WORD_MAP[c2]:
                    post_words.append(WORD_MAP[c2]); continue
                c3 = cand[:-2] if cand.endswith("es") else None
                if c3 and c3 in MOVEMENT:
                    movement_parts.append(MOVEMENT[c3]); continue
                if c3 and c3 in WORD_MAP and WORD_MAP[c3]:
                    post_words.append(WORD_MAP[c3]); continue
            post_words.append(tok)  # 未知词保留英文

    parts = prefix_mods + pre_words
    if equip:
        parts.append(equip)
    if movement:
        parts.append(movement)
    parts += suffix_mods + post_words

    result = "".join(p for p in parts if p).strip()
    return result if result else name


