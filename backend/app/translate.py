def translate_body_part(value: str) -> str:
    return BODYPART_ZH.get((value or "").strip().lower(), value or "")


def translate_equipment(value: str) -> str:
    if not value:
        return value or ""
    v = value.strip().lower()
    for ph in EQUIPMENT_SORTED:
        if ph == v or ph in v:
            return EQUIPMENT[ph]
    words = [EQUIPMENT.get(w, w) for w in re.sub(r"[^\w\s]", " ", v).replace("-", " ").split()]
    return "".join(words) if words else value


def translate_muscle(value: str) -> str:
    if not value:
        return value or ""
    return MUSCLE_ZH.get(value.strip().lower(), value)


def translate_muscle_list(values: list) -> list:
    if not values:
        return []
    return [translate_muscle(v) for v in values]
